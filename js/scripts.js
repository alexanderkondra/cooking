// Gallons to Liter converter//
var liters = function (galons) {
	return (galons * 3.785);
};
var gn = prompt ("Enter Gallons");

alert(liters(gn));


//Ounces to Grams converter//
var grams = function (ounces) {
	return (ounces * 28.35);
};
var oz = prompt ("Enter Ounces");

